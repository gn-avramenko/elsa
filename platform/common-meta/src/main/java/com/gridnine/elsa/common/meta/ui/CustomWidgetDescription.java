package com.gridnine.elsa.common.meta.ui;

public class CustomWidgetDescription {
    private String viewModel;
    private String viewSettings;
    private String viewValidation;

    public String getViewModel() {
        return viewModel;
    }

    public void setViewModel(String viewModel) {
        this.viewModel = viewModel;
    }

    public String getViewSettings() {
        return viewSettings;
    }

    public void setViewSettings(String viewSettings) {
        this.viewSettings = viewSettings;
    }

    public String getViewValidation() {
        return viewValidation;
    }

    public void setViewValidation(String viewValidation) {
        this.viewValidation = viewValidation;
    }
}
